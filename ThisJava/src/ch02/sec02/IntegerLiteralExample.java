package ch02.sec02;
//p41
public class IntegerLiteralExample {
	public static void main(String[] args) {
		int var1 = 0b1011;	//2진수 
		int var2 = 0206;	//8진수
		int var3 = 365;		//10진수
		int var4 = 0xB3;	//16진수
		
		System.out.println("val1:"+var1);
		System.out.println("val2:"+var2);
		System.out.println("val3:"+var3);
		System.out.println("val4:"+var4);
	}
}
